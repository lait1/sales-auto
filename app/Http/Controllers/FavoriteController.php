    {

        $favorite = new Favorite();
        $favorite->client_id = Auth::user()->id;
        $favorite->auto_id = $id;
        If($favorite->save()){
            return redirect()->back()->with('status', 'Автомобиль добавлен в избранное');
        }else{
            return redirect()->back()->with('status', 'Что то пошло не так');
